# config/initializers/resend.rb

# ============================================================
# Issue #A-4: Resend API クライアントの初期化設定
# ============================================================
#
# 【initializersディレクトリの役割】
# config/initializers/ 配下のファイルはRails起動時に自動で読み込まれる。
# ここでResendのAPIキーをセットすることで、アプリ全体でメール送信が使えるようになる。
# データベース接続の前に実行されるため、起動時の依存関係トラブルを防げる。
#
# 【なぜENVを使うのか】
# APIキーはソースコードに直書きしてはいけない機密情報。
# 理由:
#   - GitHubにコードを公開したとき APIキーが漏洩する
#   - キーが漏洩すると第三者にメール送信されたり課金が発生する
#   - セキュリティインシデントの原因になる
#
# ENV（環境変数）に保存することで:
#   - ソースコードには「変数名」だけを書く
#   - 実際の値は各環境（本番/開発）ごとに別途設定する
#   - Gitには値が含まれないため安全
#
# 【ENV.fetchの使い方】
# ENV.fetch("RESEND_API_KEY", nil)
#   → 環境変数 RESEND_API_KEY が存在すればその値を返す
#   → 存在しなければ nil を返す（アプリは起動できる）
#
# nilを許容する理由:
#   - 開発環境ではletter_openerを使うためAPIキー不要
#   - テスト環境でも実際のメール送信は不要
#   - APIキーがない状態でResend送信しようとするとResend側がエラーを返すため
#     想定外の送信は起きない（二重の安全策）
Resend.api_key = ENV.fetch("RESEND_API_KEY", nil)