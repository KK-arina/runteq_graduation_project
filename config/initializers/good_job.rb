# ==============================================================================
# config/initializers/good_job.rb
# ==============================================================================
#
# 【このファイルの役割】
# GoodJob（非同期ジョブ処理ライブラリ）の動作設定を定義するファイル。
# アプリ起動時に読み込まれ、GoodJob の挙動を制御する。
#
# 【GoodJob 4.x での正しい設定方法】
# Rails.application.configure ブロック経由で設定する。
# これが GoodJob 4.x で動作する公式推奨の書き方。
#
# 【execution_mode について】
# 環境ごとに異なる設定が必要なため、各環境ファイルで個別に設定する:
#   development → config/environments/development.rb で :async を設定
#   production  → config/environments/production.rb で :external を設定
#   test        → config/environments/test.rb で :test を設定（queue_adapter）
# ==============================================================================

Rails.application.configure do
  # ============================================================================
  # 同時実行数（スレッド数）の設定
  # ============================================================================
  #
  # max_threads:
  #   1つの GoodJob Worker プロセスが同時に実行できるジョブの最大数。
  #
  # 【なぜ 3 を選んだのか】
  # Render 無料プランの制約を考慮した計算:
  #   Web サービス（Puma）: Worker 2 × Thread 3 = 最大 6 コネクション使用
  #   GoodJob Worker:      3 スレッド × 1 コネクション = 最大 3 コネクション
  #   合計: 6 + 3 = 9 コネクション → Neon の上限（10〜20）以内で安全
  config.good_job.max_threads = 3

  # ============================================================================
  # ポーリング間隔の設定
  # ============================================================================
  #
  # poll_interval:
  #   GoodJob Worker が「新しいジョブはないか？」と
  #   good_jobs テーブルを確認する間隔（秒）。
  #
  # 【なぜ 30 秒か】
  # - 短すぎる（例: 1秒）→ DB への SELECT が頻繁に発生してコネクションを圧迫
  # - 長すぎる（例: 300秒）→ ジョブ実行が遅延する
  # - 30秒 → AI 分析・通知が最大 30 秒遅れる程度で UX に影響しないレベル
  #
  # 【補足: LISTEN/NOTIFY について】
  # GoodJob は polling だけでなく PostgreSQL の LISTEN/NOTIFY 機能も使う。
  # perform_later でジョブが積まれた瞬間に Worker が通知を受け取って即座に実行する。
  # poll_interval はあくまで「念のためのチェック間隔」として機能する。
  config.good_job.poll_interval = 30

  # ============================================================================
  # 完了済みジョブの保持期間の設定
  # ============================================================================
  #
  # cleanup_preserved_jobs_before_seconds_ago:
  #   正常完了・エラー終了したジョブのレコードを何秒後に削除するか。
  #   削除しないと good_jobs テーブルが肥大化して DB の容量を圧迫する。
  #
  # 86400 秒 = 24 時間（1日）
  # Neon 無料プランは容量制限があるため古いジョブは早めに削除する。
  config.good_job.cleanup_preserved_jobs_before_seconds_ago = 86_400

  # ============================================================================
  # cron（定期ジョブ）の設定
  # ============================================================================
  #
  # 【cron とは】
  # 「毎日 AM4:05 に実行」のように時刻を指定して自動的にジョブを実行する仕組み。
  #
  # 【cron 式の書き方】
  # "分 時 日 月 曜日" の順で指定する。
  # * → 任意（毎分/毎時/毎日...）、数字 → その値のとき
  #
  # 【タイムゾーンの変換（JST → UTC）】
  # GoodJob の cron はデフォルトで UTC を使用する。
  # JST は UTC+9 のため、JST の時刻から 9 時間引いた UTC 時刻を指定する。
  #
  #   JST 04:05 → UTC 19:05（前日）→ cron: "5 19 * * *"
  #   JST 00:05 → UTC 15:05（前日）→ cron: "5 15 * * *"
  #   JST 00:00 → UTC 15:00（前日）→ cron: "0 15 * * *"
  #   JST 03:00 → UTC 18:00（前日）→ cron: "0 18 * * *"
  config.good_job.cron = {
    # --------------------------------------------------------------------------
    # ① ストリーク日次計算ジョブ
    # --------------------------------------------------------------------------
    # 毎日 AM 4:05（JST）に全ユーザーのストリークを計算・更新する。
    # AM 4:00 が「1日の境界」なので 4:05 に実行することで
    # 前日分の習慣記録がすべて確定した後に正確な値が算出できる。
    streak_daily_calculation: {
      cron: "5 19 * * *",
      class: "StreakCalculationJob",
      description: "毎日 JST AM4:05 にストリーク（連続達成日数）を計算・更新する"
    },

    # --------------------------------------------------------------------------
    # ② 日次通知カウントリセットジョブ
    # --------------------------------------------------------------------------
    # 毎日 JST 00:05 に user_settings.daily_notification_count を 0 にリセットする。
    daily_notification_count_reset: {
      cron: "5 15 * * *",
      class: "DailyNotificationCountResetJob",
      description: "毎日 JST 00:05 に daily_notification_count を 0 にリセットする"
    },

    # --------------------------------------------------------------------------
    # ③ 月次 AI 使用回数リセットジョブ
    # --------------------------------------------------------------------------
    # 毎日 JST 00:00 に実行し、ジョブ内部で「今日が1日か」をチェックして月初のみリセット。
    monthly_ai_count_reset: {
      cron: "0 15 * * *",
      class: "MonthlyAiCountResetJob",
      description: "毎日 JST 00:00 に実行。月初（1日）のみ ai_analysis_count を 0 にリセットする"
    },

    # --------------------------------------------------------------------------
    # ④ 完了済みジョブのクリーンアップ
    # --------------------------------------------------------------------------
    # 毎日 JST 03:00（深夜の低アクセス帯）に 24時間以上前の完了済みジョブを削除する。
    cleanup_finished_jobs: {
      cron: "0 18 * * *",
      class: "GoodJob::CleanupJobsJob",
      description: "毎日 JST 03:00 に 24 時間以上前の完了済みジョブを削除する"
    }
  }
end