# config/initializers/good_job.rb
#
# ==============================================================================
# config/initializers/good_job.rb（G-4: RestModeExpiryJob の cron 追加）
# ==============================================================================
#
# 【G-4 での変更内容】
#   ⑥ お休みモード期限切れ自動解除ジョブを追加
#      毎日 JST AM4:10 に実行し、rest_mode_until が過ぎたレコードをクリアする。
# ==============================================================================

Rails.application.configure do
  # ============================================================================
  # 同時実行数（スレッド数）の設定
  # ============================================================================
  config.good_job.max_threads = 3

  # ============================================================================
  # ポーリング間隔の設定
  # ============================================================================
  config.good_job.poll_interval = 30

  # ============================================================================
  # 完了済みジョブの保持期間の設定
  # ============================================================================
  config.good_job.cleanup_preserved_jobs_before_seconds_ago = 86_400

  # ============================================================================
  # cron（定期ジョブ）の設定
  # ============================================================================
  config.good_job.cron = {
    # --------------------------------------------------------------------------
    # ① ストリーク日次計算ジョブ
    # --------------------------------------------------------------------------
    streak_daily_calculation: {
      cron: "5 19 * * *",
      class: "StreakCalculationJob",
      description: "毎日 JST AM4:05 にストリーク（連続達成日数）を計算・更新する"
    },

    # --------------------------------------------------------------------------
    # ② 日次通知カウントリセットジョブ
    # --------------------------------------------------------------------------
    daily_notification_count_reset: {
      cron: "5 15 * * *",
      class: "DailyNotificationCountResetJob",
      description: "毎日 JST 00:05 に daily_notification_count を 0 にリセットする"
    },

    # --------------------------------------------------------------------------
    # ③ 月次 AI 使用回数リセットジョブ
    # --------------------------------------------------------------------------
    monthly_ai_count_reset: {
      cron: "0 15 * * *",
      class: "MonthlyAiCountResetJob",
      description: "毎日 JST 00:00 に実行。月初（1日）のみ ai_analysis_count を 0 にリセットする"
    },

    # --------------------------------------------------------------------------
    # ④ 完了済みジョブのクリーンアップ
    # --------------------------------------------------------------------------
    cleanup_finished_jobs: {
      cron: "0 18 * * *",
      class: "GoodJob::CleanupJobsJob",
      description: "毎日 JST 03:00 に 24 時間以上前の完了済みジョブを削除する"
    },

    # --------------------------------------------------------------------------
    # ⑤ 週次レポートメール送信ジョブ（G-2 追加）
    # --------------------------------------------------------------------------
    weekly_report_mail: {
      cron:        "0 0 * * 1",
      class:       "WeeklyReportJob",
      description: "毎週月曜日 JST AM9:00 に週次レポートメールを送信する"
    },

    # --------------------------------------------------------------------------
    # ⑥ お休みモード期限切れ自動解除ジョブ（G-4 追加）
    # --------------------------------------------------------------------------
    #
    # 【実行タイミング】
    #   毎日 JST AM4:10 に実行する。
    #   AM4:05 のストリーク計算ジョブ（①）の直後に実行することで、
    #   「お休みモード終了日当日」のストリーク計算が完了してから
    #   お休みモードを解除できる（計算順序の保証）。
    #
    # 【UTC 変換の計算】
    #   JST 04:10 = UTC 19:10（JST は UTC+9 のため 9 時間引く）
    #   cron: "10 19 * * *"
    #     → 分:10 時:19 日:* 月:* 曜日:*
    #     → UTC 19:10 毎日 = JST 04:10 毎日
    rest_mode_expiry: {
      cron:        "10 19 * * *",
      class:       "RestModeExpiryJob",
      description: "毎日 JST AM4:10 に rest_mode_until が過ぎた設定を自動解除する"
    },

    # --------------------------------------------------------------------------
    # ⑦ パーソナライズAIプロファイル週次更新ジョブ（H-8 追加）
    # --------------------------------------------------------------------------
    #
    # 【実行タイミング】
    #   毎週月曜日 JST AM4:15 に実行する。
    #   WeeklyReportJob（AM9:00）より前に実行することで、
    #   週次レポートメールが送信される前にプロファイルが最新状態になる。
    #
    # 【UTC 変換の計算】
    #   JST 04:15 = UTC 19:15（JST は UTC+9 のため 9 時間引く）
    #   cron: "15 19 * * 1"
    #     → 分:15 時:19 日:* 月:* 曜日:1（月曜日）
    #     → UTC 19:15 毎週月曜 = JST 04:15 毎週月曜
    #
    # 【なぜ JST AM4:15 か】
    #   AM4:05: StreakCalculationJob（ストリーク計算）
    #   AM4:10: RestModeExpiryJob（お休みモード解除）
    #   AM4:15: UpdateAiProfileJob（プロファイル更新）← ここ
    #   AM9:00: WeeklyReportJob（週次レポートメール）
    #   この順番で実行することで、最新の習慣データを元にプロファイルを更新できる。
    update_ai_profiles: {
      cron:        "15 19 * * 1",
      class:       "UpdateAiProfileJob",
      description: "毎週月曜日 JST AM4:15 に全ユーザーのパーソナライズAIプロファイルを更新する"
    }
  }
end