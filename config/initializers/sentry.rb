# config/initializers/sentry.rb
#
# ==============================================================================
# Issue #I-5: Sentry 初期化設定
# ==============================================================================
#
# 【このファイルの役割】
#   Sentry SDK を起動し、「どのエラーを・どの環境で・どこへ送るか」を定義する。
#   config/initializers/ 配下は Rails 起動時に自動で読み込まれる。
#
# 【なぜ全体を if ENV["SENTRY_DSN"].present? で囲むのか】
#   DSN（送信先URL）が無い環境（＝ローカル開発・テスト）では Sentry を
#   そもそも初期化しない。これにより:
#     ・開発中に自分のPCのエラーが Sentry に飛ぶ事故を防ぐ
#     ・無料枠（月5,000件）を開発ノイズで消費しない
#   本番（Render）だけ SENTRY_DSN を設定するので、本番だけ有効になる。
# ==============================================================================

if ENV["SENTRY_DSN"].present?
  Sentry.init do |config|
    # --------------------------------------------------------------------------
    # dsn: エラーの送信先
    # --------------------------------------------------------------------------
    # Sentry プロジェクトごとに発行される URL。Render の環境変数から読み込む。
    # コードに直書きすると GitHub に漏れるため、必ず環境変数経由にする。
    config.dsn = ENV["SENTRY_DSN"]

    # --------------------------------------------------------------------------
    # breadcrumbs_logger: エラー直前の「足あと」を記録する
    # --------------------------------------------------------------------------
    # エラーが起きるまでに何が起きていたか（ログ・外部HTTP通信）を
    # パンくずリストとして一緒に送る。原因調査が格段に楽になる。
    #   :active_support_logger … Rails のログを足あととして記録
    #   :http_logger           … 外部API（Gemini/Groq/LINE等）への通信を記録
    config.breadcrumbs_logger = [:active_support_logger, :http_logger]

    # --------------------------------------------------------------------------
    # enabled_environments: 送信を許可する環境を本番だけに限定する
    # --------------------------------------------------------------------------
    # 【二重の安全策】
    #   上の if で dev/test は初期化されないが、万一 SENTRY_DSN が
    #   誤って dev/test に設定されても、ここで production 以外は送信しない。
    #   Sentry は RAILS_ENV から自動で環境名を判定するため、
    #   Render（RAILS_ENV=production）でのみ "production" と判定され送信される。
    config.enabled_environments = %w[production]

    # --------------------------------------------------------------------------
    # excluded_exceptions: 通知しない例外（ノイズ削減）
    # --------------------------------------------------------------------------
    # 【重要】ActiveRecord::RecordNotFound と ActionController::RoutingError は
    #   Sentry の既定除外リスト（Sentry::Configuration::IGNORE_DEFAULT）に
    #   元から含まれている。ここで明示的に追記しているのは
    #     ① 「404系は意図的に除外している」という意図をコード上に残すため
    #     ② 公式が「= で上書きせず += で追記せよ」と推奨しているため
    #   （+= なので既定の除外リストは維持され、ここに列挙した分が追加される）
    #
    # これらは「存在しないURL/レコードへのアクセス」で、攻撃者のスキャンや
    # ユーザーの打ち間違いで日常的に発生する。監視すべき本物の障害ではないため除外する。
    config.excluded_exceptions += [
      "ActiveRecord::RecordNotFound",
      "ActionController::RoutingError"
    ]

    # --------------------------------------------------------------------------
    # traces_sample_rate: パフォーマンス監視のサンプリング率
    # --------------------------------------------------------------------------
    # 0.1 = 本番リクエストの10%だけトレース（処理時間の内訳）を収集する。
    # 100%収集すると無料枠を消費しすぎるため、10%に絞る（Issue #I-5 指定）。
    config.traces_sample_rate = 0.1

    # --------------------------------------------------------------------------
    # send_default_pii: 個人情報を既定で送らない
    # --------------------------------------------------------------------------
    # false にすると、リクエストのCookie・ユーザーのIPアドレス・送信パラメータ本文
    # などの個人情報（PII）を Sentry に送らない。
    # 本アプリはメールアドレス等を扱うため、プライバシー保護として false を明示する。
    # （どのユーザーで起きたかは、後述の render_500 で「user.id だけ」を付与して追う）
    config.send_default_pii = false

    # --------------------------------------------------------------------------
    # release: どのデプロイ（コミット）で起きたエラーかを記録する
    # --------------------------------------------------------------------------
    # 【Render での自動取得】
    #   Render はデプロイ時にコミットSHAを環境変数 RENDER_GIT_COMMIT へ
    #   自動でセットする（Render公式のデフォルト環境変数）。
    #   これを release に使うことで「このバグは v○○ のデプロイから発生」と特定でき、
    #   デプロイと不具合の相関が Sentry の画面で追える。
    #   SENTRY_RELEASE を手動設定した場合はそちらを優先する。
    #   （Issue 原文の "fly deploy 時に SENTRY_RELEASE" は Render の
    #     RENDER_GIT_COMMIT 自動提供に読み替えている）
    config.release = ENV["SENTRY_RELEASE"].presence || ENV["RENDER_GIT_COMMIT"].presence
  end
end