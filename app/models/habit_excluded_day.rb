# app/models/habit_excluded_day.rb
#
# ==============================================================================
# HabitExcludedDay（習慣の除外日）モデル
# ==============================================================================
# 【このモデルの役割】
#   習慣ごとに「この曜日は実施しない」という除外設定を管理する。
#   例: 土曜・日曜は筋トレをしない → day_of_week: [6, 0] を保存する。
#
# 【テーブル設計】
#   habit_id    : 対象の習慣（外部キー）
#   day_of_week : 曜日番号（0=日曜, 1=月曜, ..., 6=土曜）
#                 ※ Ruby の Date#wday と同じ体系を採用
#
# 【UNIQUE制約について】
#   DB レベルで (habit_id, day_of_week) の組み合わせを一意に保証している。
#   これにより「同じ習慣の同じ曜日が重複登録される」バグを防ぐ。
#   制約は A-1 のマイグレーション済みのため、ここでは defines だけ行う。
#
# 【曜日番号の対応表】
#   0 = 日曜日 (Sunday)
#   1 = 月曜日 (Monday)
#   2 = 火曜日 (Tuesday)
#   3 = 水曜日 (Wednesday)
#   4 = 木曜日 (Thursday)
#   5 = 金曜日 (Friday)
#   6 = 土曜日 (Saturday)
# ==============================================================================

class HabitExcludedDay < ApplicationRecord
  # ============================================================
  # アソシエーション
  # ============================================================

  # belongs_to :habit
  # 【理由】
  #   habit_excluded_days テーブルは habits テーブルの子テーブル。
  #   1つの除外日設定は必ず1つの習慣に属する。
  #   belongs_to を宣言することで habit_excluded_day.habit と書けるようになる。
  belongs_to :habit

  # ============================================================
  # バリデーション
  # ============================================================

  # day_of_week は 0〜6 の整数のみ許可する
  # 【理由】
  #   0〜6 以外の値（例: 7, -1, nil）が入ると曜日の意味をなさない。
  #   presence: true → nil を弾く
  #   inclusion: { in: 0..6 } → 0〜6 の範囲外の値を弾く
  validates :day_of_week,
            presence: true,
            inclusion: {
              in:      0..6,
              message: "は0〜6の整数（0=日曜〜6=土曜）で指定してください"
            }

  # 同じ習慣で同じ曜日の重複登録を防ぐ（アプリ層での二重防御）
  # 【理由】
  #   DB レベルでも UNIQUE 制約があるが、アプリ層でもチェックすることで
  #   ユーザーへの分かりやすいエラーメッセージを返せる。
  #   DB エラー（ActiveRecord::RecordNotUnique）は汎用的なメッセージしか出せないため、
  #   モデルバリデーションで先にチェックする「二重防御」が望ましい。
  validates :day_of_week,
            uniqueness: {
              scope:   :habit_id,
              message: "はすでに除外日として設定されています"
            }

  # ============================================================
  # 定数
  # ============================================================

  # 曜日番号 → 日本語名のマッピング
  # 【理由】
  #   ビューで day_of_week（数値）を日本語に変換するために使う。
  #   例: HabitExcludedDay::DAY_NAMES[0] → "日"
  DAY_NAMES = {
    0 => "日",
    1 => "月",
    2 => "火",
    3 => "水",
    4 => "木",
    5 => "金",
    6 => "土"
  }.freeze

  # DAY_NAMES_FULL: フォームのラベル用（フルネーム）
  # 【理由】
  #   チェックボックスのラベルとして「日曜」「月曜」と表示するために使う。
  DAY_NAMES_FULL = {
    0 => "日曜",
    1 => "月曜",
    2 => "火曜",
    3 => "水曜",
    4 => "木曜",
    5 => "金曜",
    6 => "土曜"
  }.freeze
end