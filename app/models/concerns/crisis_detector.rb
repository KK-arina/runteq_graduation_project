# app/models/concerns/crisis_detector.rb
#
# ==============================================================================
# CrisisDetector（危機ワード検出モジュール）
# ==============================================================================
#
# 【このモジュールの役割】
#   振り返り入力・PMVV 入力のテキストに「死にたい」「消えたい」などの
#   危機ワードが含まれているかを検出する。
#
# 【なぜ Concern として切り出すのか】
#   WeeklyReflection と UserPurpose の両方で同じ検出ロジックが必要なため、
#   DRY（Don't Repeat Yourself）原則に従って共通モジュールにまとめる。
#   将来 Habit モデルなど他のモデルにも適用しやすくなる。
#
# 【設計の重要方針】
#   ① 検出はあくまで「サポート情報の提示」のトリガーであり、
#      ユーザーの入力を妨害しない（「入力を続ける」で継続可能）。
#   ② crisis_detected フラグを DB に記録することで
#      運営者がサポートの必要なユーザーを把握できる。
#   ③ AI 分析はスキップするが、振り返り・PMVV の保存自体は続行する。
#
# 【参考文献・相談窓口情報】
#   よりそいホットライン: https://www.yorisoi-hotline.jp/ （24時間・無料・0120-279-338）
#   いのちの電話: https://www.inochi.or.jp/ （毎日16時〜21時、毎月10日24時間・0120-783-556）
# ==============================================================================

module CrisisDetector
  extend ActiveSupport::Concern

  # ============================================================
  # CRISIS_KEYWORDS: 危機ワードのリスト
  # ============================================================
  #
  # 【選定基準】
  #   厚生労働省の自殺対策ガイドライン・各相談窓口が公開している
  #   「危機サイン」として挙げられているキーワードを参考にしている。
  #   参考: https://www.mhlw.go.jp/stf/seisakunitsuite/bunya/hukushi_kaigo/seikatsuhogo/jisatsu/index.html
  #
  # 【バリエーションを網羅する理由】
  #   日本語は表記ゆれが多い（ひらがな・カタカナ・漢字混在）。
  #   「消えたい」「きえたい」「キエタイ」のように複数の表記で
  #   入力される可能性があるため、主要なバリエーションを登録する。
  #   完全網羅は難しいため、定期的な見直しを推奨する。
  #
  # 【freeze について】
  #   freeze を付けることで配列・文字列の内容が変更不可（イミュータブル）になる。
  #   定数は変更されるべきではないため、誤った変更を防ぐために freeze する。
  CRISIS_KEYWORDS = [
    # --- 自傷・自殺に関するワード ---
    "死にたい", "しにたい", "シニタイ",
    "死んでしまいたい", "しんでしまいたい",
    "死んでしまいたかった",
    "殺したい", "ころしたい",
    "自殺", "じさつ",
    "首を吊る", "くびをつる",
    "リストカット", "りすとかっと",
    "過剰服薬", "ODしたい", "おーでぃーしたい",

    # --- 消滅・消去に関するワード ---
    "消えたい", "きえたい", "キエタイ",
    "消えてしまいたい", "きえてしまいたい",
    "消えてしまいたかった",
    "存在を消したい", "いなくなりたい",
    "いなくなってしまいたい",

    # --- 絶望・生きることへの否定 ---
    "生きていたくない", "いきていたくない",
    "生きていられない", "もう生きられない",
    "死ぬしかない", "しぬしかない",
    "もう終わりにしたい", "終わりにしたい",
    "楽になりたい死",

    # --- 遺書・別れに関するワード ---
    "遺書", "いしょ",
    "最後のお別れ", "さいごのおわかれ"
  ].freeze

  # ============================================================
  # crisis_keywords_pattern
  # ============================================================
  #
  # 【正規表現の構築】
  #   CRISIS_KEYWORDS を Regexp.union で1つの正規表現パターンに結合する。
  #   Regexp.escape で各ワードを安全にエスケープ（メタ文字を無効化）してから
  #   union で OR 結合する。
  #
  #   例: /死にたい|消えたい|しにたい|.../
  #
  # 【なぜクラスメソッドではなくモジュールメソッドにするのか】
  #   このパターンは CRISIS_KEYWORDS が変わらない限り毎回同じ値になる。
  #   モジュール内のメソッドとして定義することで、
  #   インクルードされた各モデルから呼べるようにする。
  CRISIS_PATTERN = Regexp.union(CRISIS_KEYWORDS.map { |kw| Regexp.escape(kw) }).freeze

  # ============================================================
  # included ブロック: インクルードされたモデルに機能を注入する
  # ============================================================
  #
  # 【included の役割】
  #   このモジュールを include したモデル（WeeklyReflection・UserPurpose 等）に
  #   インスタンスメソッドを追加する。
  included do
    # ── attr_accessor :crisis_word_detected ──────────────────────────────────
    # 【役割】
    #   危機ワードが検出されたかどうかを示す一時的なフラグ。
    #   DB カラムではなく Ruby オブジェクトのインメモリ属性として保持する。
    #
    # 【なぜ DB カラムではないのか】
    #   このフラグはフォーム送信後のリダイレクト前に JavaScript に伝えるために使う。
    #   最終的な crisis_detected は AiAnalysis テーブルに記録される。
    #   モデル自体には crisis_detected カラムは不要。
    attr_accessor :crisis_word_detected

    # ── before_validation :check_crisis_keywords ─────────────────────────────
    # 【なぜ before_save ではなく before_validation か】
    #   before_validation は before_save より早く実行される。
    #   バリデーション前に :crisis_word_detected をセットすることで、
    #   コントローラーがバリデーション通過後のオブジェクトを確認するときに
    #   このフラグを参照できる。
    #
    #   before_save だとバリデーション後なので、
    #   バリデーションエラー時にフラグが立たない問題が起きる可能性がある。
    before_validation :check_crisis_keywords
  end

  # ============================================================
  # インスタンスメソッド
  # ============================================================

  # crisis_word_detected?
  # 【役割】
  #   :crisis_word_detected フラグが true かどうかを返す述語メソッド。
  #   コントローラーや Service からこのメソッドで検出状態を確認する。
  #
  # 【!!（二重否定）の理由】
  #   attr_accessor の初期値は nil なので、!!(nil) = false になる。
  #   true/false のブール値を確実に返すために !! を使う。
  def crisis_word_detected?
    !!crisis_word_detected
  end

  private

  # ── check_crisis_keywords ────────────────────────────────────────────────
  # 【役割】
  #   モデルが持つテキストフィールドを結合し、
  #   CRISIS_PATTERN と照合して危機ワードの有無を検出する。
  #   検出された場合は :crisis_word_detected を true にセットする。
  #
  # 【crisis_text_fields について】
  #   各モデルで検索対象のフィールドが異なる。
  #   インクルードされた側のモデルが crisis_text_fields を定義することで、
  #   どのフィールドを検索対象にするかを制御できる。
  #   定義がない場合は空配列にフォールバックする。
  def check_crisis_keywords
    # 各モデルが定義する crisis_text_fields を呼び出す
    # respond_to? で定義されていない場合は [] を返す（安全なフォールバック）
    fields = respond_to?(:crisis_text_fields, true) ? crisis_text_fields : []

    # 全フィールドの内容を結合して1つの文字列にする
    # compact で nil を除去し、join で連結する
    combined_text = fields.compact.join(" ")

    # CRISIS_PATTERN で検出する
    # match? は一致した場合に true を返す（マッチオブジェクトを生成しないため高速）
    self.crisis_word_detected = CRISIS_PATTERN.match?(combined_text)
  end
end