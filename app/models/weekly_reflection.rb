# frozen_string_literal: true

# ==============================================================================
# WeeklyReflection（週次振り返り）モデル
#
# 【このファイルの役割】
# weekly_reflectionsテーブルに対応するモデルクラス。
# Railsでは「モデル = データの操作・ビジネスロジックの置き場所」という設計思想。
#
# 【主な機能】
# 1. アソシエーション: Userとの1対多の関係を定義
# 2. バリデーション: データの整合性をアプリケーションレベルで担保
# 3. スコープ: よく使う検索条件を名前付きで定義
# 4. クラスメソッド: 週の開始日・終了日の自動計算
# 5. インスタンスメソッド: 振り返りの状態管理
# ==============================================================================
class WeeklyReflection < ApplicationRecord

  # ============================================================================
  # アソシエーション（関連付け）
  # ============================================================================

  # --------------------------------------------------------------------------
  # belongs_to :user
  # 「WeeklyReflectionはUserに属する」という関係を定義する。
  # これにより weekly_reflection.user でユーザー情報を取得できる。
  # Railsはbelongs_toの場合、デフォルトでuser_idのNULLチェックも行う。
  # --------------------------------------------------------------------------
  belongs_to :user

  # ============================================================================
  # バリデーション（入力値の検証）
  #
  # バリデーションはDB保存前にアプリ側でデータの正しさを確認する仕組み。
  # DBのNOT NULL制約と二重でチェックすることで、より安全なデータ管理ができる。
  # ============================================================================

  # --------------------------------------------------------------------------
  # week_start_dateのバリデーション
  # presence: true → 必須チェック（空・nilは許可しない）
  # --------------------------------------------------------------------------
  validates :week_start_date, presence: true

  # --------------------------------------------------------------------------
  # week_end_dateのバリデーション
  # presence: true → 必須チェック
  # --------------------------------------------------------------------------
  validates :week_end_date, presence: true

  # --------------------------------------------------------------------------
  # reflection_commentのバリデーション
  # length: { maximum: 1000 } → 1000文字以内に制限
  # allow_blank: true → 空文字・nilの場合はバリデーションをスキップ（任意入力）
  # --------------------------------------------------------------------------
  validates :reflection_comment,
            length: { maximum: 1000 },
            allow_blank: true

  # --------------------------------------------------------------------------
  # is_lockedのバリデーション
  # inclusion: { in: [true, false] } → true or false のみ許可
  # ※ presence: true だとfalseがバリデーションエラーになるため、
  #   booleanにはinclusionを使うのがRailsのベストプラクティス
  # --------------------------------------------------------------------------
  validates :is_locked, inclusion: { in: [true, false] }

  # --------------------------------------------------------------------------
  # user_id + week_start_dateの組み合わせをユニークにするバリデーション
  # DBのUNIQUE制約と二重チェックすることで、
  # 同時リクエスト時のレースコンディション以外は確実に重複を防止できる
  # --------------------------------------------------------------------------
  validates :week_start_date,
            uniqueness: {
              scope: :user_id,
              message: 'この週の振り返りは既に作成されています'
            }

  # --------------------------------------------------------------------------
  # カスタムバリデーション: 週の範囲が必ず7日間（6日後）であることを確認する
  #
  # 【なぜ必要か】
  # week_start_date と week_end_date はそれぞれ presence チェックしているが、
  # 例えば start: 2026-02-16, end: 2026-02-10 のような論理的に不正な
  # データも通ってしまう。
  # このバリデーションで「必ずstart + 6日 = end」になることを強制する。
  # --------------------------------------------------------------------------
  validate :week_range_must_be_one_week

  # ============================================================================
  # スコープ（よく使う検索条件を名前付きで定義）
  #
  # スコープを使うと、コントローラーやビューで
  # WeeklyReflection.completed のようにシンプルに書けるようになる。
  # ============================================================================

  # --------------------------------------------------------------------------
  # completedスコープ（振り返り完了済みのレコードを取得）
  # is_locked: true = 振り返りを完了してロックが解除された状態
  # --------------------------------------------------------------------------
  scope :completed, -> { where(is_locked: true) }

  # --------------------------------------------------------------------------
  # pendingスコープ（振り返り未完了のレコードを取得）
  # is_locked: false = まだ振り返りが完了していない状態
  # --------------------------------------------------------------------------
  scope :pending, -> { where(is_locked: false) }

  # --------------------------------------------------------------------------
  # recentスコープ（新しい順に並び替え）
  # week_start_dateの降順 = 直近の週から古い週の順に取得
  # --------------------------------------------------------------------------
  scope :recent, -> { order(week_start_date: :desc) }

  # --------------------------------------------------------------------------
  # for_weekスコープ（特定の週のレコードを取得）
  # 引数にweek_start_date（月曜日の日付）を渡すと該当週のレコードを返す
  # --------------------------------------------------------------------------
  scope :for_week, ->(start_date) { where(week_start_date: start_date) }

  # ============================================================================
  # クラスメソッド（インスタンスを必要とせず、モデル自体が持つメソッド）
  # ============================================================================

  class << self
    # ------------------------------------------------------------------------
    # current_week_start_date
    # 「今週の月曜日」をAM4:00基準で返すクラスメソッド。
    #
    # 【AM4:00基準とは】
    # HabitFlowでは深夜活動を前日扱いにするため、
    # AM4:00を1日の境界とする設計になっている。
    # 例: 月曜日AM3:59 → まだ日曜日扱い → 先週の月曜日を返す
    #     月曜日AM4:00 → 月曜日扱い → 今週の月曜日を返す
    #
    # 戻り値: Date型（例: 2026-02-16）
    # ------------------------------------------------------------------------
    def current_week_start_date
      # HabitRecord.today_for_record は既に実装済みのAM4:00基準の「今日」を返すメソッド
      # 別モデルのメソッドを呼び出すことで、日付計算ロジックを一元管理している
      today = HabitRecord.today_for_record
      today.beginning_of_week(:monday)
    end

    # ------------------------------------------------------------------------
    # current_week_end_date
    # 「今週の日曜日」をAM4:00基準で返すクラスメソッド。
    #
    # 戻り値: Date型（例: 2026-02-22）
    # ------------------------------------------------------------------------
    def current_week_end_date
      current_week_start_date + 6.days
    end

    # ------------------------------------------------------------------------
    # week_start_date_for(date)
    # 引数で渡した日付が属する週の月曜日を返す汎用メソッド。
    #
    # 引数: date - Date型またはTime型
    # 戻り値: Date型
    # ------------------------------------------------------------------------
    def week_start_date_for(date)
      date.to_date.beginning_of_week(:monday)
    end

    # ------------------------------------------------------------------------
    # week_end_date_for(date)
    # 引数で渡した日付が属する週の日曜日を返す汎用メソッド。
    #
    # 引数: date - Date型またはTime型
    # 戻り値: Date型
    # ------------------------------------------------------------------------
    def week_end_date_for(date)
      week_start_date_for(date) + 6.days
    end

    # ------------------------------------------------------------------------
    # find_or_build_for_current_week(user)
    # 今週分の振り返りレコードを取得、なければ新規作成（未保存）して返す。
    #
    # 引数: user - Userインスタンス
    # 戻り値: WeeklyReflectionインスタンス（保存済みまたは未保存）
    # ------------------------------------------------------------------------
    def find_or_build_for_current_week(user)
      start_date = current_week_start_date
      end_date   = current_week_end_date

      user.weekly_reflections.find_or_initialize_by(
        week_start_date: start_date
      ) do |reflection|
        # ブロック内は新規作成時のみ実行される
        reflection.week_end_date = end_date
      end
    end
  end

  # ============================================================================
  # インスタンスメソッド（個々のレコードに対して呼べるメソッド）
  # ============================================================================

  # --------------------------------------------------------------------------
  # complete!
  # 振り返りを「完了」状態にして保存するメソッド。
  # bang（!）メソッドは保存失敗時に例外を発生させる。
  # --------------------------------------------------------------------------
  def complete!
    update!(is_locked: true)
  end

  # --------------------------------------------------------------------------
  # completed?
  # 振り返りが完了済みかどうかを返すメソッド。
  # 戻り値: true（完了済み）/ false（未完了）
  # --------------------------------------------------------------------------
  def completed?
    is_locked
  end

  # --------------------------------------------------------------------------
  # pending?
  # 振り返りが未完了かどうかを返すメソッド。
  # 戻り値: true（未完了）/ false（完了済み）
  # --------------------------------------------------------------------------
  def pending?
    !is_locked
  end

  # --------------------------------------------------------------------------
  # week_label
  # 画面表示用に「2026/02/16 - 02/22」形式の文字列を返すメソッド。
  # 戻り値: String型（例: "2026/02/16 - 02/22"）
  # --------------------------------------------------------------------------
  def week_label
    "#{week_start_date.strftime('%Y/%m/%d')} - #{week_end_date.strftime('%m/%d')}"
  end

  # ============================================================================
  # private（外部から直接呼び出せないメソッド）
  #
  # 【privateとは】
  # クラスの内部処理のみで使うメソッドを定義する。
  # バリデーションのコールバックはRailsが内部で呼ぶため、privateで問題ない。
  # ============================================================================
  private

  # --------------------------------------------------------------------------
  # week_range_must_be_one_week
  # week_end_date が week_start_date の6日後であることを検証するカスタムバリデーション。
  #
  # 【なぜカスタムバリデーションか】
  # 標準バリデーション（presence, length等）では
  # 「2つのカラムの関係」を検証できないため、
  # validate :メソッド名 の形式でカスタム実装する。
  #
  # 【処理の流れ】
  # 1. どちらかが空ならチェックをスキップ（他のバリデーションに任せる）
  # 2. end_date が start_date + 6日と一致しなければエラーを追加
  # --------------------------------------------------------------------------
  def week_range_must_be_one_week
    # blank? は nil または空文字のときtrueを返すメソッド
    # 片方でも空の場合は presence バリデーションに任せてスキップする
    return if week_start_date.blank? || week_end_date.blank?

    # week_end_date が start_date の6日後でなければエラーを追加する
    # errors.add(カラム名, メッセージ) でバリデーションエラーを登録する
    unless week_end_date == week_start_date + 6.days
      errors.add(:week_end_date, 'は開始日の6日後でなければなりません')
    end
  end
end