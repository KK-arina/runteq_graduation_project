# ==============================================================================
# HabitRecord モデル
# ==============================================================================
# 【役割】
# - 日次の習慣記録を管理するモデル
# - チェック型習慣の「やった/やらない」を記録
# - AM 4:00 を1日の境界として扱う特殊な日付計算ロジックを実装
# 
# 【MVP範囲】
# - チェック型習慣のみ対応（completed カラム）
# - 数値型習慣（value カラム）はMVP後に追加予定
# 
# 【データ整合性】
# - UNIQUE制約: 同じユーザーが同じ習慣を同じ日に複数回記録できない
# - 外部キー制約: ユーザーまたは習慣が削除されたら自動的にレコードも削除（DB + アプリの二重保証）
# ==============================================================================
class HabitRecord < ApplicationRecord
  # ============================================================================
  # アソシエーション（関連付け）
  # ============================================================================
  # 【belongs_to :user】
  # - HabitRecord は User に所属する（1対多の「多」側）
  # - 必須: user_id が必ず必要（optional: false がデフォルト）
  # - 参照: users テーブルの id カラム
  # 
  # 【belongs_to :habit】
  # - HabitRecord は Habit に所属する（1対多の「多」側）
  # - 必須: habit_id が必ず必要
  # - 参照: habits テーブルの id カラム
  # ============================================================================
  belongs_to :user
  belongs_to :habit

  # ============================================================================
  # バリデーション（データの妥当性チェック）
  # ============================================================================
  
  # --------------------------------------------------------------------------
  # 1. record_date の存在チェック
  # --------------------------------------------------------------------------
  # - presence: true → 記録日は必須（空欄不可）
  # --------------------------------------------------------------------------
  validates :record_date, presence: true

  # --------------------------------------------------------------------------
  # 2. record_date のユニーク制約（アプリケーションレベル）
  # --------------------------------------------------------------------------
  # - uniqueness: { scope: [:user_id, :habit_id] }
  #   → 同じユーザー（user_id）が同じ習慣（habit_id）を同じ日（record_date）に
  #     複数回記録できないようにする
  # 
  # 【DB制約との違い】
  # - DB制約: マイグレーションファイルで add_index unique: true
  # - アプリ制約: モデルで validates uniqueness
  # - 両方設定することで二重の安全策
  # --------------------------------------------------------------------------
  validates :record_date, uniqueness: { scope: [:user_id, :habit_id] }

  # --------------------------------------------------------------------------
  # 3. completed の包含チェック
  # --------------------------------------------------------------------------
  # - inclusion: { in: [true, false] }
  #   → completed は true または false のいずれかのみ許可
  # - boolean型のカラムに対する厳密なチェック
  # 
  # 【なぜ必要か】
  # - boolean型でも nil が入る可能性があるため
  # - 明示的に true/false のみを許可する
  # --------------------------------------------------------------------------
  validates :completed, inclusion: { in: [true, false] }

  # ============================================================================
  # スコープ（検索条件の再利用）
  # ============================================================================
  
  # --------------------------------------------------------------------------
  # 特定の日付の記録を取得
  # --------------------------------------------------------------------------
  # 【使用例】
  # - HabitRecord.for_date(Date.current)
  #   → 今日の記録を全て取得
  # - user.habit_records.for_date(Date.yesterday)
  #   → 特定ユーザーの昨日の記録を取得
  # --------------------------------------------------------------------------
  scope :for_date, ->(date) { where(record_date: date) }

  # --------------------------------------------------------------------------
  # 特定のユーザーの記録を取得
  # --------------------------------------------------------------------------
  # 【使用例】
  # - HabitRecord.for_user(user)
  #   → 特定ユーザーの記録を全て取得
  # - HabitRecord.for_user(user).for_date(Date.current)
  #   → 特定ユーザーの今日の記録を取得
  # --------------------------------------------------------------------------
  scope :for_user, ->(user) { where(user: user) }

  # ============================================================================
  # クラスメソッド（HabitRecord.メソッド名 で呼び出す）
  # ============================================================================
  
  # ----------------------------------------------------------------------------
  # AM 4:00 基準の「今日」の日付を取得
  # ----------------------------------------------------------------------------
  # 【目的】
  # - 深夜の活動を前日の記録として扱う
  # - 例: 2024/1/1 AM 3:59 → 2023/12/31
  # - 例: 2024/1/1 AM 4:00 → 2024/1/1
  # 
  # 【ロジック】
  # 1. 現在時刻（Time.current）を取得
  # 2. AM 4:00 を境界とする
  # 3. 現在時刻が境界より前なら、前日の日付を返す
  # 4. 現在時刻が境界以降なら、当日の日付を返す
  # 
  # 【使用例】
  # - HabitRecord.today_for_record
  #   → 2024-01-01 AM 3:59 に実行 → Date.new(2023, 12, 31)
  #   → 2024-01-01 AM 4:00 に実行 → Date.new(2024, 1, 1)
  # 
  # 【改善点】
  # - change(hour: 4) を使用してAM 4:00の時刻を明示的に作成
  # - 将来的に境界時刻を変更する場合にも対応しやすい
  # ----------------------------------------------------------------------------
  def self.today_for_record
    # 現在時刻を取得（Rails の Time.current はタイムゾーン対応）
    now = Time.current
    
    # AM 4:00 を境界とする時刻を作成
    # - change(hour: 4) → 今日の AM 4:00:00
    boundary = now.change(hour: 4, min: 0, sec: 0)
    
    # 現在時刻が境界より前かどうかをチェック
    if now < boundary
      # 前日の日付を返す（1日前にする）
      now.to_date - 1.day
    else
      # 当日の日付を返す
      now.to_date
    end
  end

  # ----------------------------------------------------------------------------
  # 特定のユーザー・習慣・日付の記録を検索または作成
  # ----------------------------------------------------------------------------
  # 【目的】
  # - 既に記録があれば取得、なければ新規作成
  # - チェックボックスのON/OFF切り替え時に使用
  # 
  # 【引数】
  # - user: User オブジェクト
  # - habit: Habit オブジェクト
  # - date: Date オブジェクト（デフォルト: AM4:00基準の今日）
  # 
  # 【戻り値】
  # - HabitRecord オブジェクト（既存 or 新規作成）
  # 
  # 【使用例】
  # - record = HabitRecord.find_or_create_for(user, habit)
  #   → 今日の記録を取得 or 作成
  # - record = HabitRecord.find_or_create_for(user, habit, Date.new(2024, 1, 1))
  #   → 2024/1/1 の記録を取得 or 作成
  # ----------------------------------------------------------------------------
  def self.find_or_create_for(user, habit, date = today_for_record)
    # find_or_create_by! メソッドを使用
    # - 条件に一致するレコードがあれば取得
    # - なければ新規作成
    # - ! 付きなので、失敗時は例外を発生（エラーハンドリングしやすい）
    find_or_create_by!(
      user: user,
      habit: habit,
      record_date: date
    )
  end

  # ============================================================================
  # インスタンスメソッド（record.メソッド名 で呼び出す）
  # ============================================================================
  
  # ----------------------------------------------------------------------------
  # 完了状態を切り替え（トグル）
  # ----------------------------------------------------------------------------
  # 【目的】
  # - チェックボックスのON/OFF切り替え
  # - true → false、false → true
  # 
  # 【使用例】
  # - record.toggle_completed!
  #   → completed が true なら false に、false なら true に変更して保存
  # 
  # 【戻り値】
  # - 保存成功: true
  # - 保存失敗: 例外を発生（Rails の toggle! メソッドの仕様）
  # 
  # 【改善点】
  # - Rails標準の toggle! メソッドを使用
  # - より簡潔で Rails らしい書き方
  # ----------------------------------------------------------------------------
  def toggle_completed!
    # toggle! メソッドで completed を反転して保存
    # - ! 付きなので、バリデーションエラー時は例外を発生
    # - Rails標準メソッドなので保守性が高い
    toggle!(:completed)
  end
end
