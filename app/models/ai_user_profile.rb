# ==============================================================================
# AiUserProfile（パーソナライズAIプロファイル）モデル
# ==============================================================================
class AiUserProfile < ApplicationRecord
  # ============================================================
  # アソシエーション
  # ============================================================
  belongs_to :user

  # ============================================================
  # バリデーション
  # ============================================================

  # DB の UNIQUE インデックスと二重で防御する
  validates :user_id, uniqueness: true

  # ============================================================
  # インスタンスメソッド
  # ============================================================

  # stale?
  # ----------------------------------------------------------
  # 【役割】
  #   このプロファイルが「7日以上更新されていない（古い）」か判定する。
  #
  # 【定数を UserContextBuilderService::STALE_DAYS で参照する理由】
  #   「7日」という閾値を AiUserProfile 側と UserContextBuilderService 側で
  #   別々の数値リテラルで書くと、将来どちらかを変更したときに
  #   もう一方を忘れてバグになる（定数の二重管理）。
  #   定数は1か所だけで管理するため、サービス側の定数を参照する。
  # ----------------------------------------------------------
  def stale?
    return true if analyzed_at.nil?

    analyzed_at < UserContextBuilderService::STALE_DAYS.days.ago
  end
end