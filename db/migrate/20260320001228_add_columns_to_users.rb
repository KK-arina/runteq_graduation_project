# ==============================================================================
# 【このマイグレーションの目的】
# users テーブルに本リリース用の新しいカラムを追加する。
#
# 【追加するカラムと理由】
#
#   provider      : OmniAuth（Google/LINEログイン）でどのサービス経由か記録する
#                   例: 'email'（通常登録）/ 'google'（Googleログイン）/ 'line'（LINEログイン）
#
#   uid           : OmniAuth で各プロバイダが発行するユーザー識別子
#                   例: Google の sub（subject）ID や LINE の user ID
#                   provider と uid の組み合わせで「誰がどのサービスでログインしたか」を一意に識別する
#
#   line_user_id  : LINE Messaging API でプッシュ通知を送るためのユーザーID
#                   ★重要★ LINEログイン（uid）とは別もの
#                   LINEログイン用uid = 認証のためのID（ログイン機能）
#                   line_user_id     = 通知送信のためのID（メッセージング機能）
#                   両方同じアカウントでも「用途が違うID」として分けて管理する
#
#   first_login_at: 初回ログイン日時。NULL = 未完了（オンボーディング表示が必要）
#                   オンボーディング（初回案内）の完了判定に使用する
#                   完了したら現在時刻を保存し、以降はオンボーディングをスキップする
#
# 【既存データへの影響】
# add_column でカラムを追加するだけなので、既存レコードは壊れない。
# 追加したカラムはすべて NULL 許可のため、既存ユーザーのデータはそのまま残る。
# ==============================================================================

class AddColumnsToUsers < ActiveRecord::Migration[7.2]
  def change
    # ─────────────────────────────────────────────────────────────────────────
    # provider カラムを追加
    # ─────────────────────────────────────────────────────────────────────────
    # string 型: 短い文字列を保存する型（デフォルト最大255文字）
    # default: 'email': 通常のメール登録ユーザーはデフォルトで 'email' とする
    #   → 既存ユーザーも自動的に 'email' が設定されるため既存データが壊れない
    # null: false にしない理由: 既存ユーザーレコードとの互換性を保つため
    #   → add_column 後にデフォルト値で埋めてから NOT NULL 化する方法もあるが、
    #     今回はシンプルに NULL 許可で進める
    add_column :users, :provider, :string, default: 'email'

    # ─────────────────────────────────────────────────────────────────────────
    # uid カラムを追加
    # ─────────────────────────────────────────────────────────────────────────
    # string 型: OmniAuth が返す識別子（Google の sub ID など）を保存する
    # NULL 許可: 通常のメール登録ユーザーは uid を持たないため NULL を許可する
    add_column :users, :uid, :string

    # ─────────────────────────────────────────────────────────────────────────
    # line_user_id カラムを追加
    # ─────────────────────────────────────────────────────────────────────────
    # string 型: LINE Messaging API の通知送信に使うユーザーID
    # NULL 許可: LINE 通知を使わないユーザーは NULL のまま
    add_column :users, :line_user_id, :string

    # ─────────────────────────────────────────────────────────────────────────
    # first_login_at カラムを追加
    # ─────────────────────────────────────────────────────────────────────────
    # datetime 型: 日付と時刻を同時に保存できる型
    #   例: 2026-03-19 09:41:00 のような形式で保存される
    # NULL 許可: NULL = オンボーディング未完了 という設計
    #   → 既存ユーザーは NULL になり、オンボーディング済み扱いにしたい場合は
    #     別途データ移行（seeds や console）で対応する
    add_column :users, :first_login_at, :datetime

    # ─────────────────────────────────────────────────────────────────────────
    # INDEX の追加: (provider, uid) の複合インデックス
    # ─────────────────────────────────────────────────────────────────────────
    # 【なぜこのインデックスが必要か？】
    # OmniAuth ログイン時に「このプロバイダのこの uid のユーザーを探す」という
    # クエリが必ず発生する:
    #   WHERE provider = 'google' AND uid = 'xxx'
    #
    # (provider, uid) の複合インデックスを作ることで、このクエリが高速になる。
    # unique: true → 同じ provider + uid の組み合わせが重複しないよう制約をかける
    #   → 同じ Google アカウントで2つのユーザーが作られることを防ぐ
    # name: 明示的に名前をつける理由は PostgreSQL のインデックス名の上限が63文字のため
    #   自動生成名が長くなる場合に備えてショートネームを使う
    add_index :users,
              [ :provider, :uid ],
              unique: true,
              name: 'index_users_on_provider_and_uid'
  end
end
