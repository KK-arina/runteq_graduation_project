  def change
    # -------------------------------------------------------------------------
    # weekly_reflectionsテーブルを作成する
    # create_table はRailsがSQLの「CREATE TABLE」文を生成するメソッド
    # idカラム（主キー）は自動で追加される（明示的に書かなくてよい）
    # -------------------------------------------------------------------------
    create_table :weekly_reflections do |t|
      # -----------------------------------------------------------------------
      # user_id（外部キー）
      # 「誰の」振り返りかを紐付けるためのカラム。
      # references を使うと外部キー制約が自動で設定される。
      # null: false → 必ずユーザーが存在しないといけない（NULL禁止）
      # foreign_key: true → DBレベルでusersテーブルとの整合性を保証する
      # on_delete: :cascade → ユーザーを削除したとき、紐づく振り返りも自動削除
      #   ※ これはHabitRecordと同じ設計方針。データの孤立を防ぐため。
      # -----------------------------------------------------------------------
      t.references :user,
                   null: false,
                   foreign_key: { on_delete: :cascade }

      # -----------------------------------------------------------------------
      # week_start_date（週の開始日）
      # 「何週分」の振り返りかを識別するための日付カラム。
      # HabitFlowでは月曜日をAM4:00基準の週の開始日とする設計。
      # 例: 2026-02-16（月曜日）
      # null: false → 必須カラム（どの週の振り返りか不明なレコードは許可しない）
      # -----------------------------------------------------------------------
      t.date :week_start_date, null: false

      # -----------------------------------------------------------------------
      # week_end_date（週の終了日）
      # week_start_dateから6日後（日曜日）を保存する。
      # 例: 2026-02-22（日曜日）
      # 「week_start_date + 6日」で計算できるが、
      # 毎回計算せずDBに保存することで、期間での検索クエリが高速になる。
      # null: false → 必須カラム
      # -----------------------------------------------------------------------
      t.date :week_end_date, null: false

      # -----------------------------------------------------------------------
      # reflection_comment（振り返りコメント）
      # ユーザーが入力する「今週の反省・気づき」などのフリーテキスト。
      # text型にすることで長い文章も保存できる（string型は255文字制限あり）。
      # null: true（デフォルト）→ 任意入力にする
      #   ※ バリデーションで1000文字以内の制限はアプリ側で行う
      # -----------------------------------------------------------------------
      t.text :reflection_comment

      # -----------------------------------------------------------------------
      # is_locked（ロック状態フラグ）
      # PDCA強制ロック機能のためのフラグ。
      # true  → 振り返り完了済み（ロック解除状態）
      # false → 振り返り未完了（ロック中 = 新規習慣・タスクの追加不可）
      #
      # 【設計上の注意】
      # is_locked の命名は「ロックされているか」を表すが、
      # HabitFlowでは「振り返りを完了したか」という意味で使用する。
      # default: false → 作成時はデフォルトで「未完了（ロック中）」とする
      # null: false → NULLは許可しない（true/falseのどちらかが必須）
      # -----------------------------------------------------------------------
      t.boolean :is_locked, null: false, default: false

      # -----------------------------------------------------------------------
      # timestamps
      # created_at（レコード作成日時）と updated_at（最終更新日時）を
      # 自動で追加してくれる便利なマクロ。
      # Railsが更新時に自動でupdated_atを更新してくれる。
      # -----------------------------------------------------------------------
      t.timestamps
    end

    # -------------------------------------------------------------------------
    # UNIQUE制約: user_id + week_start_date の組み合わせを一意にする
    #
    # 【なぜ必要か】
    # 同じユーザーが同じ週に複数の振り返りを登録できないようにするため。
    # 例: user_id=1, week_start_date=2026-02-16 のレコードは1つしか作れない。
    #
    # unique: true → 重複を禁止するユニーク制約をDBレベルで設定
    # name: 'index_weekly_reflections_on_user_id_and_week_start_date'
    #   → インデックス名を明示することで、長すぎる自動生成名を回避
    # -------------------------------------------------------------------------
    add_index :weekly_reflections,
              %i[user_id week_start_date],
              unique: true,
              name: 'index_weekly_reflections_on_user_id_and_week_start_date'
  end
end
