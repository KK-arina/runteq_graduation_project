# 【このマイグレーションの目的】
# habit_excluded_days テーブルを新規作成する。
#
# 【このテーブルの役割】
# 習慣ごとに「実施しない曜日」を保存する。
# 例: 「筋トレ」は土日はやらない → day_of_week に 6（土）と 0（日）を登録する
#
# 【なぜ別テーブルにするのか？】
# 「除外曜日」は 0〜7 件と可変なため、habits テーブルのカラムに収めるのは難しい。
# 例えば配列を1カラムに詰め込む方法もあるが、検索・更新が複雑になる。
# 別テーブルにすることで「特定の曜日を除外している習慣を検索する」なども容易になる。
#
# 【day_of_week の値の意味】
#   0 → 日曜日
#   1 → 月曜日
#   2 → 火曜日
#   3 → 水曜日
#   4 → 木曜日
#   5 → 金曜日
#   6 → 土曜日
# （Ruby の Date クラスおよび Rails の wday と同じ規約）
# ==============================================================================

class CreateHabitExcludedDays < ActiveRecord::Migration[7.2]
  def change
    create_table :habit_excluded_days do |t|

      # ─────────────────────────────────────────────────────────────────────
      # habit_id: どの習慣の除外曜日か（外部キー）
      # ─────────────────────────────────────────────────────────────────────
      # t.references を使うと habit_id カラム + インデックス + 外部キー制約を一括設定できる
      # null: false → 習慣が紐づかない除外曜日レコードは存在しない
      # foreign_key: { on_delete: :cascade }
      #   → 習慣（habit）が削除されたら、紐づく除外曜日レコードも一緒に削除する
      #     データの「孤立（習慣が存在しないのに除外曜日だけ残る）」を防ぐ
      t.references :habit,
                   null: false,
                   foreign_key: { on_delete: :cascade }

      # ─────────────────────────────────────────────────────────────────────
      # day_of_week: 除外する曜日（0〜6 の整数）
      # ─────────────────────────────────────────────────────────────────────
      # integer 型: 0（日曜）〜 6（土曜）の値を保存する
      # null: false → 曜日の指定なし（NULL）は意味がないため必須
      t.integer :day_of_week, null: false

      # created_at・updated_at を自動で追加する
      t.timestamps
    end

    # ─────────────────────────────────────────────────────────────────────────
    # UNIQUE 制約: (habit_id, day_of_week) の組み合わせを一意にする
    # ─────────────────────────────────────────────────────────────────────────
    # 同じ習慣に同じ曜日を重複して登録できないようにする
    # 例: 「筋トレ」の土曜除外が2回登録されることを防ぐ
    # unique: true → 重複を DB レベルで禁止する（アプリ側バリデーションの保険）
    add_index :habit_excluded_days,
              [:habit_id, :day_of_week],
              unique: true,
              name: 'index_habit_excluded_days_on_habit_id_and_day_of_week'
  end
