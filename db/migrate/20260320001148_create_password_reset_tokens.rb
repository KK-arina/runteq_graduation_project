# パスワードリセット機能で使用するトークンを管理するテーブル。
#
# 【パスワードリセットの流れ】
# 1. ユーザーが「パスワードを忘れた」フォームにメールアドレスを入力する
# 2. サーバーがランダムなトークンを生成し、このテーブルに保存する
# 3. Resend（メール送信サービス）でトークンを含むリセット用URLをメール送信する
#    例: https://habitflow.onrender.com/password_reset/edit?token=xxxx
# 4. ユーザーがリンクをクリックし、新しいパスワードを入力する
# 5. サーバーがトークンを確認し（有効期限・使用済みチェック）、パスワードを更新する
# 6. 使用済みのトークンを is_used: true にする（再利用を防ぐ）
#
# 【セキュリティ設計】
#   expires_at  : トークンには24時間の有効期限を設ける（期限切れは使えない）
#   is_used     : 一度使ったトークンは再利用できない
#   UNIQUE 制約 : 同じトークン文字列が重複しないよう保証する
# ==============================================================================

